package com.LibrariyManagamentLogin.model;

public class Uyeler {

    private int Uye_no;
    private String Uye_adi;
    private String Uye_soyadi;
    private int Cinsiyet;
    private int Adres_no;
    private String Telefon;
    private String E_posta;

    public int getUye_no() {
        return Uye_no;
    }

    public void setUye_no(int uye_no) {
        Uye_no = uye_no;
    }

    public String getUye_adi() {
        return Uye_adi;
    }

    public void setUye_adi(String uye_adi) {
        Uye_adi = uye_adi;
    }

    public String getUye_soyadi() {
        return Uye_soyadi;
    }

    public void setUye_soyadi(String uye_soyadi) {
        Uye_soyadi = uye_soyadi;
    }

    public int getCinsiyet() {
        return Cinsiyet;
    }

    public void setCinsiyet(int cinsiyet) {
        Cinsiyet = cinsiyet;
    }

    public int getAdres_no() {
        return Adres_no;
    }

    public void setAdres_no(int adres_no) {
        Adres_no = adres_no;
    }

    public String getTelefon() {
        return Telefon;
    }

    public void setTelefon(String telefon) {
        Telefon = telefon;
    }

    public String getE_posta() {
        return E_posta;
    }

    public void setE_posta(String e_posta) {
        E_posta = e_posta;
    }
}
